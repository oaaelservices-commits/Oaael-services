// Utility functions for SEO
export function getSeoTitle(pageTitle?: string, category?: string): string {
  if (pageTitle && category) {
    return `${pageTitle} | ${category} | شركة عزل أسطح بالرياض`
  }
  if (pageTitle) {
    return `${pageTitle} | شركة عزل أسطح بالرياض`
  }
  return seoConfig.metaTemplates.home.title
}

export function getSeoDescription(pageDescription?: string, defaultType: 'home' | 'services' | 'blog' = 'home'): string {
  return pageDescription || seoConfig.metaTemplates[defaultType].description
}

export function getCanonicalUrl(path: string = ''): string {
  return `${seoConfig.website.url}${path}`
}

export function getCombinedKeywords(...additionalKeywords: string[]): string[] {
  return [
    ...seoConfig.primaryKeywords,
    ...seoConfig.longTailKeywords.slice(0, 5),
    ...additionalKeywords,
  ]
}

